// **1. For Loop**

// Tampilkan angka 1–20 menggunakan loop `for`.

// ```jsx
// For Loop
for (let i = 1; i <= 20; i++) {
    console.log(i);
}


// **2. While Loop**

// Tampilkan hitung mundur dari angka 10–1 menggunakan `while`.

// ```jsx
// // While Loop

let i = 10;
while (i >= 1) {
    console.log(i);
    i--;
}


// **3. Do–While Loop**

// Tampilkan “Halo!” minimal sekali meskipun kondisi awal salah.

// ```jsx
// // Do-While Loop
let count = 0;
do {
    console.log("Halo!");
    count++;
} while (count < 0);


// **4. Break & Continue**

// Buat loop 1–10 dengan syarat:

// - Skip angka 5 (continue)
// - Stop pada angka 8 (break)

//     ```jsx
//     // Break & Continue
for (let i = 1; i <= 10; i++) {
    if (i === 5) continue;
    if (i === 8) break;
    console.log(i);
}



